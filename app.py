    # 유사한 키워드 검색
    results = [e for e in solution_db.keys() if search in e]
    if results:
        selected = st.selectbox("가장 유사한 감정을 선택하세요:", results)
        if st.button("처방 보기"):
            st.info(solution_db[selected]["공감"])
            st.write("### 🧘 처방:")
            for act in solution_db[selected]["처방"]:
                st.write(f"- {act}")
